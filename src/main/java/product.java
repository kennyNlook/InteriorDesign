
public class product {
	
		private String pname,type,amount,size;

		public product() {
			super();
		}

		public product(String pname, String type, String amount, String size) {
			super();
			this.pname = pname;
			this.type = type;
			this.amount = amount;
			this.size = size;
		}

		public String getPname() {
			return pname;
		}

		public void setPname(String pname) {
			this.pname = pname;
		}

		public String getType() {
			return type;
		}

		public void setType(String type) {
			this.type = type;
		}

		public String getAmount() {
			return amount;
		}

		public void setAmount(String amount) {
			this.amount = amount;
		}

		public String getSize() {
			return size;
		}

		public void setSize(String size) {
			this.size = size;
		}
		
		
		
		
}
